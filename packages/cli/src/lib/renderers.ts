// ─── Shared helpers for non-Claude renderers ───

function renderStructureSection(ir: ContextIR, lines: string[]): void {
  const pagesLayer = ir.architecture.layers.find(l => l.type === 'pages');
  const apiLayer = ir.architecture.layers.find(l => l.type === 'api');
  const dbLayer = ir.architecture.layers.find(l => l.type === 'db');

  if (pagesLayer || apiLayer || dbLayer) {
    lines.push('## Project Structure');
    if (pagesLayer) {
      lines.push(`- **Pages (${pagesLayer.items.length}):** ${pagesLayer.items.join(' ')}`);
    }
    if (apiLayer) {
      lines.push(`- **API Routes (${apiLayer.items.length}):** ${apiLayer.items.join(', ')}`);
    }
    if (dbLayer) {
      lines.push(`- **DB Tables (${dbLayer.items.length}):** ${dbLayer.items.join(', ')}`);
    }
    lines.push('');
  }

  // Code metrics
  if (ir.codeMap.totalFiles > 0) {
    lines.push('## Codebase');
    lines.push(`${ir.codeMap.totalFiles} source files, ~${ir.codeMap.totalLines.toLocaleString()} lines`);
    if (ir.codeMap.largestFiles.length > 0) {
      const top = ir.codeMap.largestFiles.slice(0, 5)
        .map(f => `${f.path.split('/').pop()} (${f.lines} lines)`)
        .join(', ');
      lines.push(`Largest: ${top}`);
    }
    lines.push('');
  }
}

function renderKeyModulesSection(ir: ContextIR, lines: string[]): void {
  if (ir.architecture.hubModules.length > 0) {
    lines.push('## Key Modules');
    lines.push('Most imported modules:');
    for (const hub of ir.architecture.hubModules.slice(0, 10)) {
      lines.push(`- \`${hub.path}\` (${hub.importedBy} imports)`);
    }
    lines.push('');
  }
}

function renderActiveWorkSection(ir: ContextIR, lines: string[]): void {
  const inProgress = ir.activeWork.tasks.filter(t => t.status === 'in_progress');
  const todo = ir.activeWork.tasks.filter(t => t.status === 'todo');

  if (inProgress.length > 0 || todo.length > 0) {
    lines.push('## Active Work');
    if (inProgress.length > 0) {
      lines.push('In progress:');
      for (const t of inProgress) {
        lines.push(`- ${t.title}`);
      }
    }
    if (todo.length > 0) {
      const display = todo.slice(0, 5);
      lines.push('To do:');
      for (const t of display) {
        lines.push(`- ${t.title}`);
      }
      if (todo.length > 5) {
        lines.push(`- ... and ${todo.length - 5} more`);
      }
    }
    lines.push('');
  }

  if (ir.activeWork.git) {
    const g = ir.activeWork.git;
    lines.push('## Git');
    lines.push(`Branch: ${g.branch} | ${g.totalCommits} commits | ${g.recentCommits} this week`);
    if (g.uncommittedChanges > 0) {
      lines.push(`${g.uncommittedChanges} uncommitted changes`);
    }
    lines.push('');
  }

  renderExternalIssuesSection(ir, lines);
}

function renderExternalIssuesSection(ir: ContextIR, lines: string[]): void {
  const issues = ir.activeWork.externalIssues;
  if (!issues || issues.length === 0) return;

  lines.push('## GitHub Issues');
  for (const issue of issues.slice(0, 15)) {
    const labels = issue.labels.length > 0 ? ` [${issue.labels.join(', ')}]` : '';
    lines.push(`- #${issue.number}: ${issue.title}${labels}`);
  }
  if (issues.length > 15) {
    lines.push(`- ... and ${issues.length - 15} more`);
  }
  lines.push('');
}

function renderFocusSection(ir: ContextIR, lines: string[]): void {
  if (!ir.focusAreas || ir.focusAreas.length === 0) return;

  lines.push('## Focus Areas');
  lines.push('*Task-linked context: files and modules relevant to active work*');
  lines.push('');

  for (const area of ir.focusAreas) {
    lines.push(`### ${area.taskTitle}`);

    if (area.primaryFiles.length > 0) {
      lines.push('**Primary files:**');
      for (const f of area.primaryFiles) {
        lines.push(`- \`${f}\``);
      }
    }

    if (area.relatedModules.length > 0) {
      lines.push('**Related modules:**');
      for (const m of area.relatedModules) {
        lines.push(`- \`${m}\``);
      }
    }

    if (area.relevantExports.length > 0) {
      lines.push('**Key exports:**');
      for (const exp of area.relevantExports) {
        lines.push(`- ${exp.kind}: ${exp.name}`);
      }
    }

    lines.push('');
  }
}

function renderSmartSection(ir: ContextIR, lines: string[]): void {
  if (!ir.smartRecommendation) return;

  const rec = ir.smartRecommendation;
  lines.push('## Recommended Focus (Smart Context)');
  lines.push(`*${rec.workContext}*`);
  lines.push('');

  if (rec.activeFiles.length > 0) {
    lines.push('**Active files** (by relevance):');
    for (const file of rec.activeFiles) {
      const score = rec.relevanceScores.get(file) ?? 0;
      lines.push(`- \`${file}\` (score: ${score})`);
    }
    lines.push('');
  }

  if (rec.relatedModules.length > 0) {
    lines.push('**Related modules:**');
    for (const mod of rec.relatedModules) {
      lines.push(`- \`${mod}\``);
    }
    lines.push('');
  }

  if (rec.suggestedExports.length > 0) {
    lines.push('**Key exports in scope:**');
    for (const exp of rec.suggestedExports.slice(0, 15)) {
      lines.push(`- ${exp.kind}: \`${exp.name}\` in \`${exp.file}\``);
    }
    lines.push('');
  }
}

function renderConstraintsSection(ir: ContextIR, lines: string[]): void {
  // Environment variables
  if (ir.constraints.envVars.length > 0) {
    lines.push('## Environment Variables');
    lines.push(ir.constraints.envVars.join(', '));
    lines.push('');
  }

  // Scripts
  const scriptNames = Object.keys(ir.constraints.scripts);
  if (scriptNames.length > 0) {
    lines.push('## Scripts');
    for (const name of scriptNames) {
      lines.push(`- \`${name}\`: \`${ir.constraints.scripts[name]}\``);
    }
    lines.push('');
  }

  // Deploy
  if (ir.constraints.deployTarget || ir.constraints.ci) {
    lines.push('## Deployment');
    if (ir.constraints.deployTarget) lines.push(`Platform: ${ir.constraints.deployTarget}`);
    if (ir.constraints.ci) lines.push(`CI: ${ir.constraints.ci}`);
    lines.push('');
  }
}
