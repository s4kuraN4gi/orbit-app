import type { ContextIR } from './context-ir.js';
import type { FocusArea } from './task-focus.js';

export type RenderTarget = 'claude' | 'cursor' | 'cursor-mdc' | 'copilot' | 'windsurf';

export const RENDER_TARGETS: Record<RenderTarget, string> = {
  claude: 'CLAUDE.md',
  cursor: '.cursorrules',
  'cursor-mdc': '.cursor/rules/',
  copilot: '.github/copilot-instructions.md',
  windsurf: '.windsurfrules',
};

export function renderContext(ir: ContextIR, target: RenderTarget): string {
  switch (target) {
    case 'claude':
      return renderClaude(ir);
    case 'cursor':
      return renderCursor(ir);
    case 'cursor-mdc':
      // For MDC, renderContext returns the project overview as default.
      // Use renderCursorMdc() for multi-file output.
      return renderCursor(ir);
    case 'copilot':
      return renderCopilot(ir);
    case 'windsurf':
      return renderWindsurf(ir);
  }
}

/**
 * Render Cursor MDC format: returns a Map of filename → content
 * for writing to .cursor/rules/ directory.
 */
export function renderCursorMdc(ir: ContextIR): Map<string, string> {
  const files = new Map<string, string>();

  // project.mdc — Project overview + tech stack
  const projectLines: string[] = [];
  projectLines.push(`# Project: ${ir.project.name}`);
  projectLines.push('');
  if (ir.project.techStack.length > 0) {
    projectLines.push('## Tech Stack');
    projectLines.push(ir.project.techStack.join(' / '));
    const meta: string[] = [];
    if (ir.project.packageManager) meta.push(`Package manager: ${ir.project.packageManager}`);
    if (ir.constraints.deployTarget) meta.push(`Platform: ${ir.constraints.deployTarget}`);
    if (ir.constraints.ci) meta.push(`CI: ${ir.constraints.ci}`);
    if (meta.length > 0) projectLines.push(meta.join(' | '));
    projectLines.push('');
  }
  renderStructureSection(ir, projectLines);
  renderKeyModulesSection(ir, projectLines);
  renderConstraintsSection(ir, projectLines);
  files.set('project.mdc', renderMdcFile('Project overview, tech stack, and structure', projectLines.join('\n')));

  // tasks.mdc — Active tasks
  if (ir.activeWork.tasks.length > 0) {
    const taskLines: string[] = [];
    renderActiveWorkSection(ir, taskLines);
    files.set('tasks.mdc', renderMdcFile('Active tasks and current work', taskLines.join('\n')));
  }

  // focus.mdc — Focus areas (if present)
  if (ir.focusAreas && ir.focusAreas.length > 0) {
    const focusLines: string[] = [];
    renderFocusSection(ir, focusLines);
    files.set('focus.mdc', renderMdcFile('Task-linked focus areas', focusLines.join('\n')));
  }

  // smart.mdc — Smart context recommendations (if present)
  if (ir.smartRecommendation) {
    const smartLines: string[] = [];
    renderSmartSection(ir, smartLines);
    files.set('smart.mdc', renderMdcFile('AI-powered context recommendations', smartLines.join('\n')));
  }

  return files;
}

function renderMdcFile(description: string, content: string): string {
  return `---\ndescription: ${description}\nglobs: \nalwaysApply: true\n---\n\n${content}`;
}

// ─── CLAUDE.md ───
// Must produce byte-identical output to the old generateContext()

export function renderClaude(ir: ContextIR): string {
  const lines: string[] = [];
  const now = new Date().toISOString().slice(0, 10);

  lines.push(`<!-- Generated by Orbit CLI on ${now} — Do not edit above this line -->`);
  lines.push('');
  lines.push(`# Project: ${ir.project.name}`);
  lines.push('');

  // ── Tech Stack ──
  const hasStack = ir.project.techStack.length > 0 || ir.project.packageManager ||
    ir.constraints.deployTarget || ir.constraints.ci;
  if (hasStack) {
    lines.push('## Tech Stack');
    if (ir.project.techStack.length > 0) {
      lines.push(ir.project.techStack.join(' / '));
    }
    const meta: string[] = [];
    if (ir.project.packageManager) meta.push(`Package Manager: ${ir.project.packageManager}`);
    if (ir.constraints.deployTarget) meta.push(`Platform: ${ir.constraints.deployTarget}`);
    if (ir.constraints.ci) meta.push(`CI: ${ir.constraints.ci}`);
    if (meta.length > 0) {
      lines.push(`- ${meta.join('\n- ')}`);
    }
    lines.push('');
  }

  // ── Project Structure ──
  const pagesLayer = ir.architecture.layers.find(l => l.type === 'pages');
  const apiLayer = ir.architecture.layers.find(l => l.type === 'api');
  const dbLayer = ir.architecture.layers.find(l => l.type === 'db');
  const pages = pagesLayer?.items ?? [];
  const apiRoutes = apiLayer?.items ?? [];
  const dbTables = dbLayer?.items ?? [];

  if (pages.length > 0 || apiRoutes.length > 0 || dbTables.length > 0) {
    lines.push('## Project Structure');
    if (pages.length > 0) {
      const pageList = pages.length > 12
        ? pages.slice(0, 12).join(' ') + ` +${pages.length - 12}`
        : pages.join(' ');
      lines.push(`- **Pages (${pages.length}):** ${pageList}`);
    }
    if (apiRoutes.length > 0) {
      // Extract unique methods from "METHOD /path" format
      const methods = [...new Set(apiRoutes.map(r => r.split(' ')[0]))].join(', ');
      lines.push(`- **API Routes (${apiRoutes.length}):** ${methods}`);
    }
    if (dbTables.length > 0) {
      const tableNames = dbTables.join(', ');
      lines.push(`- **DB Tables (${dbTables.length}):** ${tableNames}`);
    }
    lines.push('');
  }

  // ── Key Files ──
  if (ir.codeMap.totalFiles > 0) {
    lines.push('## Key Files');
    if (ir.codeMap.largestFiles.length > 0) {
      const top = ir.codeMap.largestFiles.slice(0, 5)
        .map(f => `${f.path.split('/').pop()} (${f.lines} lines)`)
        .join(', ');
      lines.push(`- Largest: ${top}`);
    }
    lines.push(`- ${ir.codeMap.totalFiles} source files, ~${ir.codeMap.totalLines.toLocaleString()} lines`);
    lines.push('');
  }

  // ── Exports ──
  const allExports = ir.codeMap.modules.flatMap(m => m.exports);
  if (allExports.length > 0) {
    lines.push('## Exports');
    const components = allExports.filter(e => e.kind === 'component');
    const functions = allExports.filter(e => e.kind === 'function');
    const types = allExports.filter(e => e.kind === 'type');
    if (components.length > 0) {
      const display = components.slice(0, 20);
      lines.push(`- **Components (${components.length}):** ${display.map(e => e.name).join(', ')}${components.length > 20 ? ` +${components.length - 20}` : ''}`);
    }
    if (functions.length > 0) {
      const display = functions.slice(0, 20);
      lines.push(`- **Functions (${functions.length}):** ${display.map(e => e.name).join(', ')}${functions.length > 20 ? ` +${functions.length - 20}` : ''}`);
    }
    if (types.length > 0) {
      const display = types.slice(0, 20);
      lines.push(`- **Types (${types.length}):** ${display.map(e => e.name).join(', ')}${types.length > 20 ? ` +${types.length - 20}` : ''}`);
    }
    lines.push('');
  }

  // ── Import Graph Summary ──
  if (ir.architecture.importGraph.length > 0) {
    const fileCount = new Set(ir.architecture.importGraph.map(e => e.from)).size;
    const totalImports = ir.architecture.importGraph.length;
    const hubs = ir.architecture.hubModules;

    lines.push('## Import Graph');
    lines.push(`${fileCount} files, ${totalImports} local imports`);
    if (hubs.length > 0) {
      lines.push('');
      lines.push('Most imported modules:');
      for (const hub of hubs) {
        lines.push(`- \`${hub.path}\` (${hub.importedBy} imports)`);
      }
    }
    lines.push('');
  }

  // ── Focus Areas ──
  renderFocusSection(ir, lines);

  // ── Smart Context ──
  renderSmartSection(ir, lines);

  // ── Active Tasks ──
  const inProgress = ir.activeWork.tasks.filter(t => t.status === 'in_progress');
  const todo = ir.activeWork.tasks.filter(t => t.status === 'todo');
  const done = ir.activeWork.tasks.filter(t => t.status === 'done');
  const total = ir.activeWork.tasks.length;

  if (total > 0) {
    lines.push('## Active Tasks');

    if (inProgress.length > 0) {
      lines.push('### In Progress');
      for (const t of inProgress) {
        const shortId = t.id.slice(0, 7);
        lines.push(`- [ORB-${shortId}] ${t.title}`);
      }
      lines.push('');
    }

    if (todo.length > 0) {
      const display = todo.slice(0, 10);
      const remaining = todo.length - display.length;
      lines.push(`### To Do${todo.length > 10 ? ' (Top 10)' : ''}`);
      for (const t of display) {
        const shortId = t.id.slice(0, 7);
        lines.push(`- [ORB-${shortId}] ${t.title}`);
      }
      if (remaining > 0) {
        lines.push(`- ... and ${remaining} more`);
      }
      lines.push('');
    }

    // ── Completion Status ──
    const pct = total > 0 ? Math.round((done.length / total) * 100) : 0;
    lines.push('## Completion Status');
    lines.push(`${done.length}/${total} tasks done (${pct}%)`);
    lines.push('');
  } else {
    lines.push('## Tasks');
    lines.push('No task data available. Run `orbit login` + `orbit init` to include task context.');
    lines.push('');
  }

  // ── External Issues ──
  renderExternalIssuesSection(ir, lines);

  // ── Environment Variables ──
  if (ir.constraints.envVars.length > 0) {
    lines.push('## Environment Variables');
    lines.push(ir.constraints.envVars.join(', '));
    lines.push('');
  }

  // ── Scripts ──
  const scriptNames = Object.keys(ir.constraints.scripts);
  if (scriptNames.length > 0) {
    lines.push('## Scripts');
    for (const name of scriptNames) {
      lines.push(`- \`${name}\`: \`${ir.constraints.scripts[name]}\``);
    }
    lines.push('');
  }

  lines.push('<!-- End of auto-generated context -->');
  lines.push('');

  return lines.join('\n');
}

