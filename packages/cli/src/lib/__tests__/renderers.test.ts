describe('renderCursorMdc', () => {
  it('returns a Map with project.mdc', () => {
    const files = renderCursorMdc(makeIR());
    expect(files.has('project.mdc')).toBe(true);
  });

  it('project.mdc contains MDC front-matter', () => {
    const files = renderCursorMdc(makeIR());
    const content = files.get('project.mdc')!;
    expect(content).toContain('---');
    expect(content).toContain('description: Project overview');
    expect(content).toContain('alwaysApply: true');
  });

  it('project.mdc contains project name', () => {
    const files = renderCursorMdc(makeIR());
    expect(files.get('project.mdc')).toContain('# Project: TestProject');
  });

  it('project.mdc contains tech stack', () => {
    const files = renderCursorMdc(makeIR());
    expect(files.get('project.mdc')).toContain('Next.js / React / TypeScript');
  });

  it('returns tasks.mdc when tasks are present', () => {
    const files = renderCursorMdc(makeIR());
    expect(files.has('tasks.mdc')).toBe(true);
    expect(files.get('tasks.mdc')).toContain('Implement auth flow');
  });

  it('does not return tasks.mdc when tasks are empty', () => {
    const ir = makeIR({ activeWork: { tasks: [], git: null } });
    const files = renderCursorMdc(ir);
    expect(files.has('tasks.mdc')).toBe(false);
  });

  it('returns focus.mdc when focus areas are present', () => {
    const ir = makeIR({ focusAreas: makeFocusAreas() });
    const files = renderCursorMdc(ir);
    expect(files.has('focus.mdc')).toBe(true);
    expect(files.get('focus.mdc')).toContain('Task-linked');
  });

  it('does not return focus.mdc when no focus areas', () => {
    const files = renderCursorMdc(makeIR());
    expect(files.has('focus.mdc')).toBe(false);
  });

  it('returns smart.mdc when smart recommendation is present', () => {
    const ir = makeIR({ smartRecommendation: makeSmartRecommendation() });
    const files = renderCursorMdc(ir);
    expect(files.has('smart.mdc')).toBe(true);
    expect(files.get('smart.mdc')).toContain('AI-powered');
  });

  it('does not return smart.mdc when no recommendation', () => {
    const files = renderCursorMdc(makeIR());
    expect(files.has('smart.mdc')).toBe(false);
  });
});

