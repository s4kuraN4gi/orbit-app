describe('edge cases', () => {
  it('handles empty tech stack', () => {
    const ir = makeIR({
      project: { name: 'Bare', techStack: [], packageManager: null, nodeVersion: null },
      constraints: { envVars: [], deployTarget: null, ci: null, scripts: {} },
    });
    const output = renderClaude(ir);
    expect(output).toContain('# Project: Bare');
    expect(output).not.toContain('## Tech Stack');
  });

  it('handles no tasks and no git', () => {
    const ir = makeIR({
      activeWork: { tasks: [], git: null },
    });
    const output = renderClaude(ir);
    expect(output).toContain('No task data available');
    expect(output).not.toContain('## Git');
  });

  it('handles no architecture layers', () => {
    const ir = makeIR({
      architecture: { layers: [], importGraph: [], hubModules: [] },
    });
    const output = renderClaude(ir);
    expect(output).not.toContain('## Project Structure');
    expect(output).not.toContain('## Import Graph');
  });

  it('handles no exports', () => {
    const ir = makeIR({
      codeMap: {
        modules: [],
        entryPoints: [],
        totalFiles: 0,
        totalLines: 0,
        largestFiles: [],
      },
    });
    const output = renderClaude(ir);
    expect(output).not.toContain('## Exports');
    expect(output).not.toContain('## Key Files');
  });

  it('handles no env vars', () => {
    const ir = makeIR({
      constraints: { envVars: [], deployTarget: null, ci: null, scripts: {} },
    });
    const output = renderClaude(ir);
    expect(output).not.toContain('## Environment Variables');
    expect(output).not.toContain('## Scripts');
  });

  it('handles external issues', () => {
    const ir = makeIR({
      activeWork: {
        tasks: [],
        git: null,
        externalIssues: [
          { number: 42, title: 'Fix login bug', labels: ['bug', 'priority'], body: 'Login fails', state: 'open' as const, url: 'https://github.com/test/42', provider: 'github' },
          { number: 43, title: 'Add dark mode', labels: [], body: '', state: 'open' as const, url: 'https://github.com/test/43', provider: 'github' },
        ],
      },
    });
    const output = renderClaude(ir);
    expect(output).toContain('## GitHub Issues');
    expect(output).toContain('#42: Fix login bug [bug, priority]');
    expect(output).toContain('#43: Add dark mode');
  });

  it('large task list truncates to-do items', () => {
    const tasks = Array.from({ length: 15 }, (_, i) => ({
      id: `task-${i.toString().padStart(3, '0')}`,
      title: `Task ${i}`,
      status: 'todo' as const,
      priority: 'medium',
    }));
    const ir = makeIR({
      activeWork: { tasks, git: null },
    });
    const output = renderClaude(ir);
    expect(output).toContain('### To Do (Top 10)');
    expect(output).toContain('... and 5 more');
  });

  it('cursor-mdc handles many pages correctly', () => {
    const ir = makeIR({
      architecture: {
        ...makeIR().architecture,
        layers: [
          { type: 'pages', items: Array.from({ length: 20 }, (_, i) => `/page-${i}`) },
        ],
      },
    });
    const files = renderCursorMdc(ir);
    const content = files.get('project.mdc')!;
    expect(content).toContain('**Pages (20):**');
  });
});
